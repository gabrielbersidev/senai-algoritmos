package pctEx01;

public class Exemplo01 {
	public static void main(String [] args) {
		double num1 = 5;
		double num2 = 2;
		
		System.out.println("\nOperadores:");
		System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
		System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
		System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
		System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
		System.out.println(num1 + " % " + num2 + " = " + (num1 % num2));

	}
}